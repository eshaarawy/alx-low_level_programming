#include "3-calc.h"

/**
 * op_add - adds the two integers a and b
 * @a: the first integer
 * @b: the ssecond integer
 * Return: result
*/
int op_add(int a, int b)
{
	return (a + b);
}
/**
 * op_sub - subtract the two integers a and b
 * @a: the first integer
 * @b: the ssecond integer
 * Return: result
*/
int op_sub(int a, int b)
{
	return (a - b);
}
/**
 * op_mul - multiplies the two integers a and b
 * @a: the first integer
 * @b: the ssecond integer
 * Return: result
*/
int op_mul(int a, int b)
{
	return (a * b);
}
/**
 * op_div - divide the two integers a and b
 * @a: the first integer
 * @b: the ssecond integer
 * Return: result
*/
int op_div(int a, int b)
{
	return (a / b);
}
/**
 * op_mod - modulus of the two integers a and b
 * @a: the first integer
 * @b: the ssecond integer
 * Return: result
*/
int op_mod(int a, int b)
{
	return (a % b);
}
